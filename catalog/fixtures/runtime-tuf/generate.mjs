async function listFiles(directory, prefix = "") {
  let entries;
  try { entries = await readdir(directory, { withFileTypes: true }); } catch { return []; }
  const files = [];
  for (const entry of entries) {
    const path = prefix ? `${prefix}/${entry.name}` : entry.name;
    if (entry.isDirectory()) files.push(...await listFiles(resolve(directory, entry.name), path));
    else files.push(path);
  }
  return files.sort();
}

const generatedOutputs = [...outputs.entries()].map(([path, bytes]) => [resolve(fixtureRoot, path), bytes]);
if (process.argv.includes("--check")) {
  const problems = [];
  const expectedWithinFixture = new Set([...outputs.keys()].filter((path) => !path.startsWith("../")));
  for (const file of await listFiles(resolve(fixtureRoot, "valid"), "valid")) if (!expectedWithinFixture.has(file)) problems.push(`unexpected fixture file: ${file}`);
  for (const file of await listFiles(resolve(fixtureRoot, "attacks"), "attacks")) if (!expectedWithinFixture.has(file)) problems.push(`unexpected fixture file: ${file}`);
  for (const [path, expected] of generatedOutputs) {
    let actual;
    try { actual = await readFile(path); } catch { problems.push(`missing generated fixture: ${path}`); continue; }
    if (!actual.equals(expected)) problems.push(`stale generated fixture: ${path}`);
  }
  if (problems.length) {
    console.error(problems.join("\n"));
    process.exitCode = 1;
  } else {
    console.log(`runtime TUF fixtures current: 1 valid repository, 9 attack repositories`);
  }
} else {
  await rm(resolve(fixtureRoot, "valid"), { recursive: true, force: true });
  await rm(resolve(fixtureRoot, "attacks"), { recursive: true, force: true });
  for (const [path, contents] of generatedOutputs) {
    await mkdir(dirname(path), { recursive: true });
    const temporary = `${path}.tmp`;
    await writeFile(temporary, contents);
    await rename(temporary, path);
  }
  console.log(`generated runtime TUF fixtures: 1 valid repository, 9 attack repositories`);
}
