const buildTargets = ({ version = 1, targetBytes = catalogBytes } = {}) => {
  const signed = {
    type: "targets",
    spec_version: "1.0",
    version,
    expires_unix: expiresUnix,
    targets: { "catalog.json": targetDescription(targetBytes) },
  };
  const envelope = signedEnvelope(signed, ["dev-targets"]);
  return { signed, envelope, bytes: serialize(envelope) };
};

const buildSnapshot = ({ version = 1, describedTargetsVersion = 1, targetsBytes } = {}) => {
  const signed = {
    type: "snapshot",
    spec_version: "1.0",
    version,
    expires_unix: expiresUnix,
    meta: { "targets.json": description(describedTargetsVersion, targetsBytes) },
  };
  const envelope = signedEnvelope(signed, ["dev-snapshot"]);
  return { signed, envelope, bytes: serialize(envelope) };
};

const buildTimestamp = ({
  version = 1,
  expires = expiresUnix,
  describedSnapshotVersion = 1,
  snapshotBytes,
  snapshotSha256,
  snapshotLength,
  signatures = true,
} = {}) => {
  const actual = description(describedSnapshotVersion, snapshotBytes);
  const signed = {
    type: "timestamp",
    spec_version: "1.0",
    version,
    expires_unix: expires,
    snapshot: {
      version: describedSnapshotVersion,
      length: snapshotLength ?? actual.length,
      sha256: snapshotSha256 ?? actual.sha256,
    },
  };
  const envelope = signedEnvelope(signed, signatures ? ["dev-timestamp"] : []);
  return { signed, envelope, bytes: serialize(envelope) };
};

const validTargets = buildTargets();
const validSnapshot = buildSnapshot({ targetsBytes: validTargets.bytes });
const validTimestamp = buildTimestamp({ snapshotBytes: validSnapshot.bytes });

function emitRepository(prefix, {
  root = rootBytes,
  timestamp = validTimestamp.bytes,
  snapshot = validSnapshot.bytes,
  snapshotFileVersion = 1,
  targets = validTargets.bytes,
  targetsFileVersion = 1,
  catalog = catalogBytes,
  expected = "success",
  expectedStage = "repository",
  rollbackState = { root: 0, timestamp: 0, snapshot: 0, targets: 0 },
}) {
  outputs.set(`${prefix}/runtime-root.json`, root);
  outputs.set(`${prefix}/timestamp.json`, timestamp);
  outputs.set(`${prefix}/${snapshotFileVersion}.snapshot.json`, snapshot);
  outputs.set(`${prefix}/${targetsFileVersion}.targets.json`, targets);
  outputs.set(`${prefix}/catalog.json`, catalog);
  outputs.set(`${prefix}/fixture.json`, serialize({
    _generated: generatedNotice,
    at_time_unix: atTimeUnix,
    rollback_state: rollbackState,
    expected,
    expected_stage: expectedStage,
  }));
}

